<p>Forms allow you to get information from the user. By using a php script to handle that form, you can do all kinds of cool things - log a user in, register a user, add a user to a mailing list, personalize content, etc. Getting the information from a form to a php script is simple. Only your imagination (and the willingness of users to submit the information) limit whatyou can do with the user input!.</p>
